var db *sqlx.DB

func initDB() (err error) {
	dsn := "root:123456@tcp(localhost:3306)/sql_test?charset=utf8mb4&parseTime=True"

	// 这里要注意，要使用全局的db 不能使用:= 否则新建了一个局部db
	db, err = sqlx.Connect("mysql", dsn) // = Open + Ping
	if err != nil {
		fmt.Printf("connect DB failed, err: %v\n", err)
		return err
	}
	db.SetMaxOpenConns(20)
	db.SetMaxIdleConns(10)
	return nil
}

type user struct {
	//首字母大写，可以被外部访问
	//因为在sqlx中使用反射去获取结构体的字段名，所以必须要大写！！！
	ID   int    `db:"id"` //tag 表示数据库的实际字段名
	Name string `db:"name"`
	Age  int    `db:"age"`
}

